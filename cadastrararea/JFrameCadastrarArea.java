    public void cbCondominio() {
        CondominioDAO dao = new CondominioDAO();

        try {
            for (Condominio c : dao.listar()) {
                cbCondominio.addItem(c);
            }
        } catch (DadosException ex) {
            JOptionPane.showMessageDialog(this, "Não foi possivel carregar Área. Deu ruim porque: " + ex, "Erro", JOptionPane.ERROR_MESSAGE);

        }
    }

    private Area area = new Area();

    public void editar(Area entidade) {
        String situacao;
        String ativo = "Ativo";
        String inativo = "Inativo";

        area = entidade;
        areatxt.setText(area.getArea());
        situacao = area.getSituacao();

        if (situacao.equals(ativo)) {
            Ativa.setSelected(true);
        } else if (situacao.equals(inativo)) {
            Inativa.setSelected(true);
        } else if (situacao.equals("")) {
            Ativa.setSelected(false);
            Inativa.setSelected(false);
        }

    }

