    private void SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarActionPerformed

        Object a = "--Selecione um Condomínio--";
        Object b = cbCondominio.getSelectedItem();

        if (a.equals(b)) {
            JOptionPane.showMessageDialog(this, "Selecione um Condomínio", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        } else if (b.equals(null)) {
            JOptionPane.showMessageDialog(this, "Selecione um Condomínio", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        area.setCondominio((Condominio) b);
        area.setArea(areatxt.getText());
        if (Ativa.isSelected()) {
            area.setSituacao("Ativo");
        } else if (Inativa.isSelected()) {
            area.setSituacao("Inativo");
        } else {
            JOptionPane.showMessageDialog(this, "Situação não selecionado", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        AreaBO bo = new AreaBO();

        try {
            if (area.getId() == 0) {
                bo.inserir(area);
                JOptionPane.showMessageDialog(this, "Incluido com sucesso!!", "Informação", JOptionPane.INFORMATION_MESSAGE);
            } else {
                bo.alterar(area);
                JOptionPane.showMessageDialog(this, "Alterado com sucesso!!", "Informação", JOptionPane.INFORMATION_MESSAGE);
            }
            dispose();
            JFramePesquisarArea area = new JFramePesquisarArea();
            area.setVisible(true);
        } catch (AreaException ex) {
            if (ex.getCause() == null) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Aviso", JOptionPane.WARNING_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, ex.getCause().getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_SalvarActionPerformed

